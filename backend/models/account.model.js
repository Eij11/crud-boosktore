import mongoose from "mongoose";

const dashboardDB = mongoose.connection.useDb("dashboard");

const AccountSchema = mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
    role: {
      type: String,
      default: "user",
    },
  },
  { timestamps: true }
);

export const Account = dashboardDB.model("Account", AccountSchema);
